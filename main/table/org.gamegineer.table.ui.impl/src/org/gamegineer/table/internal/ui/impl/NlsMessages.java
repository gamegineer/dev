package org.gamegineer.table.internal.ui.impl;

import net.jcip.annotations.ThreadSafe;
import org.eclipse.osgi.util.NLS;

/**
 * A utility class to manage localized messages for the package.
 */
@ThreadSafe
final class NlsMessages
    extends NLS
{
    // ======================================================================
    // Fields
    // ======================================================================

    // --- Branding ---------------------------------------------------------

    /** The default application branding name. */
    public static String Branding_name_default;

    // --- CommandLineOptions -----------------------------------------------

    /** The description for the "help" option. */
    public static String CommandLineOptions_help_description;

    // --- TableRunner ------------------------------------------------------

    /** The command line usage message. */
    public static String TableRunner_cli_usage;


    // ======================================================================
    // Constructors
    // ======================================================================

    /**
     * Initializes the {@code NlsMessages} class.
     */
    static
    {
        NLS.initializeMessages( NlsMessages.class.getName(), NlsMessages.class );
    }

    /**
     * Initializes a new instance of the {@code NlsMessages} class.
     */
    private NlsMessages()
    {
    }
}
