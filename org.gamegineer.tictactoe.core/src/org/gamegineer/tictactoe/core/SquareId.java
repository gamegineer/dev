package org.gamegineer.tictactoe.core;

/**
 * The identifier of a square on the game board.
 */
public enum SquareId
{
    // ======================================================================
    // Enum Constants
    // ======================================================================

    /** The identifier of the top-left square. */
    TOP_LEFT,

    /** The identifier of the top-center square. */
    TOP_CENTER,

    /** The identifier of the top-right square. */
    TOP_RIGHT,

    /** The identifier of the middle-left square. */
    MIDDLE_LEFT,

    /** The identifier of the middle-center square. */
    MIDDLE_CENTER,

    /** The identifier of the middle-right square. */
    MIDDLE_RIGHT,

    /** The identifier of the bottom-left square. */
    BOTTOM_LEFT,

    /** The identifier of the bottom-center square. */
    BOTTOM_CENTER,

    /** The identifier of the bottom-right square. */
    BOTTOM_RIGHT,
}
